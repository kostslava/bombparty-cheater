async function main() {
  const res = await fetch(
    "https://raw.githubusercontent.com/lukes/ISO-3166-Countries-with-Regional-Codes/master/all/all.json"
  );
  if (!res.ok) throw new Error(String(res.status));
  const rows = await res.json();
  const set = new Set();
  for (const x of CAPITALS) set.add(x);
  for (const x of GIVEN_NAMES) set.add(x);
  for (const row of rows) {
    const raw = String(row.name || "")
      .toLowerCase()
      .replace(/[^a-z]/g, "");
    if (raw.length >= 3 && raw.length <= 22) set.add(raw);
  }
  const arr = [...set].filter((w) => w.length >= 3 && w.length <= 15).sort();
  const body = `// Generated by tools/gen-blocklist.mjs
(function () {
  globalThis.__BOMB_PARTY_BLOCKLIST = new Set(${JSON.stringify(arr)});
})();
`;
  fs.writeFileSync(OUT, body, "utf8");
  console.error("Wrote", OUT, "entries:", arr.length);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
