#!/usr/bin/env node
/**
 * Builds words.js from BombParty dict.txt: full word list + bigram/trigram index.
 * Words: lowercase a–z, length 3–15, unique (case-insensitive source).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT = path.join(ROOT, "words.js");

const WORDS_PATH = process.argv[2] || path.join(ROOT, "dict.txt");

function isValidWord(w) {
  return w.length >= 3 && w.length <= 15 && /^[a-z]+$/.test(w);
}

function addTo(map, key, id) {
  let arr = map.get(key);
  if (!arr) {
    arr = [];
    map.set(key, arr);
  }
  arr.push(id);
}

function dedupeSortedArrays(map) {
  for (const [k, arr] of map) {
    arr.sort((a, b) => a - b);
    const dedup = [];
    let last = -1;
    for (const id of arr) {
      if (id !== last) {
        dedup.push(id);
        last = id;
      }
    }
    map.set(k, dedup);
  }
}

console.error("Reading", WORDS_PATH);
const raw = fs.readFileSync(WORDS_PATH, "utf8");
const seen = new Set();
const words = [];
for (const line of raw.split(/\r?\n/)) {
  const w = line.trim().toLowerCase();
  if (!isValidWord(w) || seen.has(w)) continue;
  seen.add(w);
  words.push(w);
}

console.error("Words:", words.length);

const bigram = new Map();
const trigram = new Map();

for (let id = 0; id < words.length; id++) {
  const w = words[id];
  for (let i = 0; i + 1 < w.length; i++) {
    addTo(bigram, w.slice(i, i + 2), id);
  }
  for (let i = 0; i + 2 < w.length; i++) {
    addTo(trigram, w.slice(i, i + 3), id);
  }
}

dedupeSortedArrays(bigram);
dedupeSortedArrays(trigram);

console.error("Bigram keys:", bigram.size, "Trigram keys:", trigram.size);

const payload = {
  words,
  bigram: Object.fromEntries(bigram),
  trigram: Object.fromEntries(trigram),
};
fs.writeFileSync(
  OUT,
  `// Generated by tools/build-dictionary.mjs from dict.txt — do not edit by hand.
(function () {
  const payload = ${JSON.stringify(payload)};
  globalThis.__BOMB_PARTY_DICT = payload;
})();
`,
  "utf8"
);

const stat = fs.statSync(OUT);
console.error("Wrote", OUT, (stat.size / (1024 * 1024)).toFixed(2), "MiB");
